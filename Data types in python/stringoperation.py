text = str(input("Enter a message : "))

# length of the string

length = len(text)

print(length)

# Accessing the character of the string using index number

index = text[5]

print(index)

# slicing the string

slice = text[3 :8]

print(slice)

#Reverse a string

revtxt = text[::-1]

print(revtxt)

