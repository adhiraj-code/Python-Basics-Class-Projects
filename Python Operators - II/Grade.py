# Write a program using AND operator to find the level of grade

grade = int(input("Enter your Grade : "))

if grade>= 1 and grade<= 3: 
    print("Basic level in Codingal")

elif grade>= 4 and grade<= 5:
    print("Intermediate Level in Codingal")

elif grade>= 6 and grade<=8:
    print("Advance Level in Codingal")

